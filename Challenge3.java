//Show the following pattern using only print statement
//*                   *****
//**                  ****
//***                 ***
//****                **
//*****               *

public class Challenge3 {
    public static void main(String[] args) {
        System.out.println("Right Half Pyramid");
        System.out.println("*");
        System.out.println("**");
        System.out.println("***");
        System.out.println("****");
        System.out.println("*****");


        System.out.println();
        System.out.println("Reverse Right Half Pyramid");
        System.out.println("*****");
        System.out.println("****");
        System.out.println("***");
        System.out.println("**");
        System.out.println("*");

        System.out.println();
        System.out.println("Left Half Pyramid");
        System.out.println("    *");
        System.out.println("   **");
        System.out.println("  ***");
        System.out.println(" ****");
        System.out.println("*****");
    }
}
